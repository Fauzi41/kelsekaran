	<div class="panel-body">
	<p style="text-indent:50px;">Pembuatan Kartu Tanda Penduduk atau sering Disebut KTP ini merupakan dokumen kependudukan yang memuat sistem keamanan/ pengendalian baik dari sisi administrasi ataupun teknologi informasi dengan berbasis pada database kependudukan nasional.
		penduduk hanya diperbolehkan memiliki 1 KTP yang tercantum Nomor Induk Kependudukan(NIK). NIK merupakan identitas tunggal setiap penduduk dan berlaku seumur hidup.</p>
		<p>Nomor NIK yang ada di e-KTP nantinya akan dijadikan dasar dalam penerbitan Paspor, Surat Izin Mengemudi (SIM), Nomor Pokok Wajib Pajak (NPWP), Polis Asuransi, Sertifikat atas Hak Tanah dan penerbitan dokumen identitas lainnya (Pasal 13 UU No. 23 Tahun 2006 tentang Adminduk)
		Autentikasi Kartu Identitas (e-ID) biasanya menggunakan biometrik yaitu verifikasi dan validasi sistem melalui pengenalan karakteristik fisik atau tingkah laku manusia. Ada banyak jenis pengamanan dengan cara ini, antara lain sidik jari (fingerprint), retina mata, DNA, bentuk wajah, dan bentuk gigi. Pada e-KTP, yang digunakan adalah sidik jari.</p>

		<p>Penggunaan sidik jari e-KTP lebih canggih dari yang selama ini telah diterapkan untuk SIM (Surat Izin Mengemudi). Sidik jari tidak sekedar dicetak dalam bentuk gambar (format jpeg) seperti di SIM, tetapi juga dapat dikenali melalui chip yang terpasang di kartu. Data yang disimpan di kartu tersebut telah dienkripsi dengan algoritma kriptografi tertentu. Proses pengambilan sidik jari dari penduduk sampai dapat dikenali dari chip kartu adalah sebagai berikut:

		Sidik jari yang direkam dari setiap wajib KTP adalah seluruh jari (berjumlah sepuluh), tetapi yang dimasukkan datanya dalam chip hanya dua jari, yaitu jempol dan telunjuk kanan. Sidik jari dipilih sebagai autentikasi untuk e-KTP karena alasan berikut:

			<p>1. Biaya paling murah, lebih ekonomis daripada biometrik yang lain</p>
			<p>2. Bentuk dapat dijaga tidak berubah karena gurat-gurat sidik jari akan kembali ke bentuk semula</p>
			<p>3. Unik, tidak ada kemungkinan sama walaupun orang kembar </p>

			<p>Informasi penduduk yang dicantumkan dalam e-KTP ditunjukkan pada layout kasar berikut:</p>
			<p>- Nama</p>
			<p>- Tempat/Tgl Lahir</p>
			<p>- Jenis Kelamin</p>
			<p>- Alamat (RT/RW, Kel/Desa, Kecamatan)</p>
			<p>- Agama</p>
			<p>- Status Pekerjaan</p> 
			<p>- Kewarganegaraan</p>
			<p>- Berlaku Hingga</p>
			<p>- Foto</p>
			<p>- Tanda Tangan</p>
			<p>- NIK</p>
		Untuk mendapatkan informasi di atas dari penduduk, wajib KTP harus mengisi formulir tipe F1.01.

		Selain tujuan yang hendak dicapai, manfaat e-KTP diharapkan dapat dirasakan sebagai berikut:

			<p>1. Identitas jati diri tunggal</p>
			<p>2. Tidak dapat dipalsukan</p>
			<p>3. Tidak dapat digandakan</p>
			<p>4. Dapat dipakai sebagai kartu suara dalam pemilu atau pilkada</p>
		<p>Pembuatan KTP</p>
			<p>1. Datanglah ke RT/ RW untuk mendapatkan surat pengantar pembuatan KTP.</p>
			<p>2. Bawa surat pengantar tersebut ke kantor lurah, serahkan ke petugas da nisi formulir permohonan pembuatan KTP, tanda-tangangi dan serahkan ke lurah.</p>
			<p>3. Lurah akan menandatangani surat permohonan tersebut dan menyerahkannya kembali.<p>
			<p>4. Bawa surat permohonan tersebut berserta foto kopi Kartu keluarga ke Kantor Camat, dan serahkan ke petugas. Pada dasarnya sampai disini proses pembuatn KTP baru telah selesai.</p>
			<p>5. Petugas kecamatan akan memverifikasi dan validasi data kependudukan kita, selanjutnya surat permohonan disampaikan ke camat untuk ditanda tangani dan akan diteruskan ke Dinas Pencatatan Sipil (dilakukan oleh petugas).</p>
			<p>6. Setelah semua proses diatas tugas kita hanya menunggu terbitnya KTP baru, biasanya KTP sementara (non elektronik) akan terbit dalam beberapa hari saja.</p>

			<p> Syarat-Syarat Permohonan Kartu Tanda Penduduk(KTP) </p>
				<p>1. Fotokopi Kartu Keluarga (KK).</p>
				<p>2. Menyerahkan KTP lama bagi yang perpanjangan.</p>
				<p>3. Fotokopi akta kelahiran bagi yang baru akan memiliki KTP.</p>
				<p>4. Fotokopi Akta Nikah/Akta Perkawinan bagi yang belum berusia 17 tahun.</p>
				<p>5. Bukti KTP yang rusak bagi yang memohon penggantian KTP.</p>
				<p>6. Surat Keterangan Hilang dari Kepolisian bagi yang hilang.</p>
		Khusus Orang Asing Tinggal Tetap melampirkan juga fotokopi dokumen keimigrasian seperti Pasport, Izin Tinggal Tetap, Surat Keterangan Lapor Diri Dari Mabes Polri.		
