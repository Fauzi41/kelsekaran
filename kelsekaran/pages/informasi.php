				<h2 class="panel-title" style="font-size:30px;">Bagaimana Cara Mengurus KK di Kelurahan Sekaran?</h2>
			</div>
			<div class="panel-body">
		<p style="text-indent:50px;">Bagi keluarga yang masih baru, maka hal yang sangat penting untuk diperhatikan adalah mulai mengurus Kartu Keluarga (KK) sendiri. Dengan kartu keluarga inilah segala hal yang berkaitan dengan urusan administrasi dan pelayanan kependudukan akan dibuat . Ketika anda hendak membuat KTP anggota keluarga anda, maka anda butuh kartu keluarga. Ketika anak anda akan mendaftar sekolah, maka anda juga butuh kartu keluarga. Masih banyak lagi fungsi kartu keluarga untuk menyelesaikan berbagai keperluan administrasi dan pelayanan kependudukan.
		Untuk itulah, kami sarankan untuk sesegera mungkin membuat kartu keluarga yang baru begitu anda menikah dan membangun rumah tangga yang baru.</p>
		<p>Dulu ketika kami baru saja menikah, maka kami tidak menunda untuk membuat Kartu Keluarga sendiri, dan memisahkan diri dari Kartu Keluarga milik keluarga besar kami masing -masing. Ya.. tentu saja kita harus sabar, cermat, dan telaten dalam memenuhi segala persyaratan yang dibutuhkan dan menembus birokrasi pengurusan KK baru. Nah, pada artikel kami kali ini, akan kami bagikan beberapa informasi yang dapat membantu anda semua dalam mengurus kartu keluarga (KK) yang baru.</p>
		<p>Kartu Keluarga</p>
		<p> Kartu Keluarga adalah Kartu Identitas keluarga yang memuat data tentang nama, susunan, dan hubungan dalam keluarga serta identitas anggota keluarganya.</p>
		<p> Setiap Kartu Keluarga memiliki nomor seri yang akan tetap berlaku selama tidak terjadi perubahan Kepala Keluarga. Adapun jika yang terjadi perubahan susunan keluarga dalam Kartu Keluarga, maka wajib kita laporkan kepada Dinas Kependudukan dan Pencatatan Sipil setempat, selambat – lambatnya 30 hari sejak terjadinya perubahan.
        Kartu Keluarga adalah bukti yang sah dan kuat atas status identitas keluarga dan anggota keluarga .</p>
        <p>Lalu apa yang harus kita lakukan jika ingin mengurus pembuatan Kartu Keluarga? Berikut langkah – langkahnya :</p>
        <p>Meminta surat pengantar pembuatan kartu keluarga baru ke RT setempat dan dilanjutkan distempel ke RW.</p>
        <p>Mendatangi kantor kelurahan setempat untuk mengisi dan menandatangani formulir permohonan pembuatan Kartu Keluaraga dengan membawa persyaratan – persyaratan berikut :</p>
        <p> 1.Surat pengantar dari RT/RW</p>
        <p> 2. Foto kopi buku nikah /akta perkawinan bagi yang sudah menikah</p>
        <p> 3. Surat Keterangan Pindah Datang ( bagi penduduk datang)</p>

        <p>Biaya membuat Kartu Keluarga Baru<p>
        Berdasarkan UU No 24 tahun 2013 pasal 79 A menyebutkan bahwa pengurusan dan penerbitan dokumen kependudukan ( KK, KTP-el, akte kelahiran, akte kematian, akte perkawinan, akte perceraian, dan  lain – lain) TIDAK DIPUNGUT BIAYA / GRATIS. Kalau masih ada yang dipungut biaya, silahkan laporkan ke atasannya atau kepala dinas yang bersangkutan.</p>
        <p> Bagaimana Mengurus Kartu Keluarga yang Hilang?</p>
        Untuk mengurusnya, maka kepala keluarga datang ke kentor kecamatan setempat dengan membawa :</p>
        <p>Laporan kehilangan dari kepolisian</p>
        <p>1. Pengantar RT/RT</p>
        <p>2. KTP dari salah satu orang yang ada di KK</p>
        <p>3. Formulir permohonan dari kelurahan</p>

